class BlogItem {
  final String id;
  final String scheme;
  final String title;
  final String abs;
  final String content;
  final String createdAt;
  final String thumbnail;
  final String user;
  final String commentsCount;
  final String likesCount;

  BlogItem(
    this.id,
    this.scheme,
    this.title,
    this.abs,
    this.content,
    this.createdAt,
    this.thumbnail,
    this.user,
    this.commentsCount,
    this.likesCount,
  );
}
